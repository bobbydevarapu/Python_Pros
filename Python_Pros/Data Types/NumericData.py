# Integer Operations
a, b = 10, 5
print(a + b, a - b, a * b, a // b)

# Float Operations
x, y = 3.5, 2.0
print(x + y, x - y, x * y, x / y)

# Complex Number Operations
c1, c2 = 2 + 3j, 1 - 4j
print(c1 + c2, c1 - c2, c1 * c2)

# Boolean Operations (acts as 1 and 0)
t, f = True, False
print(t + f, t * f, t and f, t or f)
