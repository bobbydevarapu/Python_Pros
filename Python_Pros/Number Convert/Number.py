# Decimal to Other Bases
num =int(float("Enter the num : "))
print("\n")
print(bin(num))  # Decimal to Binary
print(oct(num))  # Decimal to Octal
print(hex(num))  # Decimal to Hexadecimal

# Other Bases to Decimal
print(int('11001', 2))  # Binary to Decimal
print(int('31', 8))     # Octal to Decimal
print(int('19', 16))    # Hexadecimal to Decimal
